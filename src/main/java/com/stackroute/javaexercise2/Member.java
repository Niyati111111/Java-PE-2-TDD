package com.stackroute.javaexercise2;

public class Member {

        String name;
        int age;
        int salary;

        public void setName(String inputName) {
            name = inputName;
        }

        public void setAge(int inputAge) {
            age = inputAge;
        }

        public void setSalary(int inputSalary) {
            salary = inputSalary;
        }

        public String getName() {
            return name;
        }

        public int getAge() {
            return age;
        }

        public int getSalary() {
            return salary;
        }
    }



